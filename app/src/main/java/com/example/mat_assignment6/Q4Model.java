package com.example.mat_assignment6;

public class Q4Model {
    String fName;
    int image;

    public Q4Model(String fName, int image) {
        this.fName = fName;
        this.image = image;
    }

    public String getfName() {
        return fName;
    }

    public void setfName(String fName) {
        this.fName = fName;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}
